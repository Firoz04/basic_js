function condition(mark){
    if(mark >=90 && mark<=100){
        console.log("Your Grade is: A+ ");
    }else if(mark >=80 && mark<=89){
        console.log("Your Grade is: A ");
    }else if(mark >=70 && mark<=79){
        console.log("Your Grade is: A- ");
    }else{
        console.log("Your Grade is: B+  ")
    }

}
condition(70);
