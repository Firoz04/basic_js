function sum(data_one, data_two){
     const result =data_one + data_two;
     return result;
}

function sub(data_one, data_two){
    const result =data_one - data_two;
    return result;
}
function mul(data_one, data_two){
    const result =data_one * data_two;
    return result;
}
function div(data_one, data_two){
    const result =data_one / data_two;
    return result;
}
console.log("Sum is: "+ sum(10,2));
console.log("Sub is: "+ sub(10,2));
console.log("Mul is: "+ mul(10,2));
console.log("Div is: "+ div(10,2));